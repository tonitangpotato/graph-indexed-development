//! Graph repair: turn validator findings into actionable fixes.
//!
//! `validator` only detects issues. `repair` plans and applies safe fixes:
//! - **Orphan edges** (edges referencing missing nodes) — always safe to remove.
//! - **Orphan nodes** (nodes with no edges) — opt-in, restricted to "safe" node types
//!   (code/file nodes from extraction); user-authored nodes (task/issue/feature) are
//!   never auto-removed because "no edges yet" often means "not yet linked".
//! - **Duplicate nodes** (same ID appears multiple times in `graph.nodes`) — keep first
//!   occurrence, drop the rest. No edge rewriting needed: duplicate IDs already collide
//!   in lookups, so existing edges still resolve to the kept node.
//! - **Duplicate edges** (same `from`/`to`/`relation` triple) — drop extras, keep first.
//! - **Self-edges** (`from == to`) — always safe to remove.
//!
//! Cycles are NOT auto-repaired: breaking a cycle requires choosing which edge to
//! drop, which is a domain decision the user must make.
//!
//! ## Two-phase design
//!
//! 1. [`plan_repair`] — pure analysis, returns a [`RepairPlan`] describing what would
//!    change. Use this for `--dry-run` and confirmation prompts.
//! 2. [`apply_repair`] — mutates the graph according to the plan, returns a
//!    [`RepairReport`] with counts. The caller is responsible for persisting (and for
//!    backing up the DB beforehand).

use crate::graph::Graph;
use std::collections::HashSet;

/// Node types that are SAFE to auto-remove when orphaned (no edges).
///
/// Anything outside this list is considered user-authored (tasks, issues, features,
/// components) and an "orphan" status more likely means "not yet linked" than
/// "stale data". Such nodes are never auto-removed by repair.
const SAFE_ORPHAN_NODE_TYPES: &[&str] = &[
    "code", "file", "function", "method", "class", "module", "trait", "struct", "enum",
];

/// Which categories of issues should be repaired.
///
/// Each flag is independent — there is no implicit ordering or coupling. `--all` in
/// the CLI sets every flag.
#[derive(Debug, Clone, Default)]
pub struct RepairOptions {
    /// Remove edges that reference missing nodes (`missing_refs`).
    pub orphan_edges: bool,
    /// Remove nodes with no edges, restricted to [`SAFE_ORPHAN_NODE_TYPES`].
    pub orphan_nodes: bool,
    /// Drop duplicate node entries (same ID) — keep first occurrence.
    pub duplicate_nodes: bool,
    /// Drop duplicate edges (same from/to/relation triple) — keep first occurrence.
    pub duplicate_edges: bool,
    /// Remove self-referential edges (`from == to`).
    pub self_edges: bool,
}

impl RepairOptions {
    /// Enable all repair categories.
    pub fn all() -> Self {
        Self {
            orphan_edges: true,
            orphan_nodes: true,
            duplicate_nodes: true,
            duplicate_edges: true,
            self_edges: true,
        }
    }

    /// True if at least one repair category is enabled.
    pub fn any(&self) -> bool {
        self.orphan_edges
            || self.orphan_nodes
            || self.duplicate_nodes
            || self.duplicate_edges
            || self.self_edges
    }
}

/// What [`apply_repair`] would change. Generated by [`plan_repair`].
#[derive(Debug, Default, Clone)]
pub struct RepairPlan {
    /// Edges to remove because they reference a missing node. (from, to, relation)
    pub orphan_edges: Vec<(String, String, String)>,
    /// Node IDs to remove because they have no edges and are a safe type.
    pub orphan_nodes: Vec<String>,
    /// Node IDs that appear more than once; later occurrences will be dropped.
    pub duplicate_node_ids: Vec<String>,
    /// Edges that appear more than once; later occurrences will be dropped.
    /// (from, to, relation)
    pub duplicate_edges: Vec<(String, String, String)>,
    /// Self-edges to remove. (node, relation)
    pub self_edges: Vec<(String, String)>,
    /// Orphan nodes that were SKIPPED because their type is not in the safe list.
    /// Reported for transparency; not removed.
    pub skipped_unsafe_orphan_nodes: Vec<(String, String)>, // (id, node_type)
}

impl RepairPlan {
    /// Total number of changes that would be applied.
    pub fn total_changes(&self) -> usize {
        self.orphan_edges.len()
            + self.orphan_nodes.len()
            + self.duplicate_node_ids.len()
            + self.duplicate_edges.len()
            + self.self_edges.len()
    }

    /// True if the plan would not change the graph.
    pub fn is_empty(&self) -> bool {
        self.total_changes() == 0
    }
}

impl std::fmt::Display for RepairPlan {
    fn fmt(&self, f: &mut std::fmt::Formatter<'_>) -> std::fmt::Result {
        if self.is_empty() && self.skipped_unsafe_orphan_nodes.is_empty() {
            return write!(f, "✓ Nothing to repair");
        }

        let mut lines = Vec::new();

        if !self.orphan_edges.is_empty() {
            lines.push(format!(
                "• Remove {} orphan edge(s) (referencing missing nodes)",
                self.orphan_edges.len()
            ));
            for (from, to, rel) in self.orphan_edges.iter().take(5) {
                lines.push(format!("    - {} → {} ({})", from, to, rel));
            }
            if self.orphan_edges.len() > 5 {
                lines.push(format!("    ... and {} more", self.orphan_edges.len() - 5));
            }
        }
        if !self.orphan_nodes.is_empty() {
            lines.push(format!(
                "• Remove {} orphan node(s) (safe types only)",
                self.orphan_nodes.len()
            ));
            for id in self.orphan_nodes.iter().take(5) {
                lines.push(format!("    - {}", id));
            }
            if self.orphan_nodes.len() > 5 {
                lines.push(format!("    ... and {} more", self.orphan_nodes.len() - 5));
            }
        }
        if !self.duplicate_node_ids.is_empty() {
            lines.push(format!(
                "• Drop {} duplicate node entry/entries (keep first)",
                self.duplicate_node_ids.len()
            ));
            for id in self.duplicate_node_ids.iter().take(5) {
                lines.push(format!("    - {}", id));
            }
            if self.duplicate_node_ids.len() > 5 {
                lines.push(format!(
                    "    ... and {} more",
                    self.duplicate_node_ids.len() - 5
                ));
            }
        }
        if !self.duplicate_edges.is_empty() {
            lines.push(format!(
                "• Drop {} duplicate edge(s)",
                self.duplicate_edges.len()
            ));
            for (from, to, rel) in self.duplicate_edges.iter().take(5) {
                lines.push(format!("    - {} → {} ({})", from, to, rel));
            }
            if self.duplicate_edges.len() > 5 {
                lines.push(format!(
                    "    ... and {} more",
                    self.duplicate_edges.len() - 5
                ));
            }
        }
        if !self.self_edges.is_empty() {
            lines.push(format!("• Remove {} self-edge(s)", self.self_edges.len()));
            for (node, rel) in self.self_edges.iter().take(5) {
                lines.push(format!("    - {} → {} ({})", node, node, rel));
            }
            if self.self_edges.len() > 5 {
                lines.push(format!("    ... and {} more", self.self_edges.len() - 5));
            }
        }
        if !self.skipped_unsafe_orphan_nodes.is_empty() {
            lines.push(format!(
                "ℹ Skipped {} orphan node(s) (user-authored type, not auto-removable):",
                self.skipped_unsafe_orphan_nodes.len()
            ));
            for (id, ty) in self.skipped_unsafe_orphan_nodes.iter().take(5) {
                lines.push(format!("    - {} (type: {})", id, ty));
            }
            if self.skipped_unsafe_orphan_nodes.len() > 5 {
                lines.push(format!(
                    "    ... and {} more",
                    self.skipped_unsafe_orphan_nodes.len() - 5
                ));
            }
        }

        write!(
            f,
            "Repair plan ({} change(s)):\n{}",
            self.total_changes(),
            lines.join("\n")
        )
    }
}

/// What [`apply_repair`] actually changed.
#[derive(Debug, Default, Clone)]
pub struct RepairReport {
    pub orphan_edges_removed: usize,
    pub orphan_nodes_removed: usize,
    pub duplicate_nodes_merged: usize,
    pub duplicate_edges_removed: usize,
    pub self_edges_removed: usize,
}

impl RepairReport {
    pub fn total(&self) -> usize {
        self.orphan_edges_removed
            + self.orphan_nodes_removed
            + self.duplicate_nodes_merged
            + self.duplicate_edges_removed
            + self.self_edges_removed
    }
}

impl std::fmt::Display for RepairReport {
    fn fmt(&self, f: &mut std::fmt::Formatter<'_>) -> std::fmt::Result {
        if self.total() == 0 {
            return write!(f, "✓ No changes applied");
        }
        let parts = [
            ("orphan edges", self.orphan_edges_removed),
            ("orphan nodes", self.orphan_nodes_removed),
            ("duplicate nodes", self.duplicate_nodes_merged),
            ("duplicate edges", self.duplicate_edges_removed),
            ("self-edges", self.self_edges_removed),
        ];
        let mut lines = vec![format!("✓ Applied {} repair(s):", self.total())];
        for (name, count) in parts {
            if count > 0 {
                lines.push(format!("  • {} {} removed", count, name));
            }
        }
        write!(f, "{}", lines.join("\n"))
    }
}

/// Build a repair plan from the current graph state.
///
/// This is read-only. The plan describes what [`apply_repair`] would do given the
/// same `opts`.
pub fn plan_repair(graph: &Graph, opts: &RepairOptions) -> RepairPlan {
    let mut plan = RepairPlan::default();

    // Index node IDs for lookups
    let node_ids: HashSet<&str> = graph.nodes.iter().map(|n| n.id.as_str()).collect();

    // ── Orphan edges (edges with missing endpoints) ──
    if opts.orphan_edges {
        let mut seen = HashSet::new();
        for edge in &graph.edges {
            let missing = !node_ids.contains(edge.from.as_str())
                || !node_ids.contains(edge.to.as_str());
            if missing {
                let key = (edge.from.clone(), edge.to.clone(), edge.relation.clone());
                if seen.insert(key.clone()) {
                    plan.orphan_edges.push(key);
                }
            }
        }
    }

    // ── Orphan nodes (no edges) ──
    if opts.orphan_nodes {
        let connected: HashSet<&str> = graph
            .edges
            .iter()
            .flat_map(|e| [e.from.as_str(), e.to.as_str()])
            .collect();

        for node in &graph.nodes {
            if connected.contains(node.id.as_str()) {
                continue;
            }
            let ty = node.node_type.as_deref().unwrap_or("");
            if SAFE_ORPHAN_NODE_TYPES.contains(&ty) {
                plan.orphan_nodes.push(node.id.clone());
            } else {
                plan.skipped_unsafe_orphan_nodes
                    .push((node.id.clone(), ty.to_string()));
            }
        }
    }

    // ── Duplicate node IDs ──
    if opts.duplicate_nodes {
        let mut seen = HashSet::new();
        for node in &graph.nodes {
            if !seen.insert(node.id.as_str()) {
                plan.duplicate_node_ids.push(node.id.clone());
            }
        }
    }

    // ── Duplicate edges ──
    if opts.duplicate_edges {
        let mut seen = HashSet::new();
        for edge in &graph.edges {
            let key = (
                edge.from.as_str(),
                edge.to.as_str(),
                edge.relation.as_str(),
            );
            if !seen.insert(key) {
                plan.duplicate_edges.push((
                    edge.from.clone(),
                    edge.to.clone(),
                    edge.relation.clone(),
                ));
            }
        }
    }

    // ── Self-edges ──
    if opts.self_edges {
        for edge in &graph.edges {
            if edge.from == edge.to {
                plan.self_edges
                    .push((edge.from.clone(), edge.relation.clone()));
            }
        }
    }

    plan
}

/// Apply a repair plan to the graph in-place.
///
/// Order of operations is chosen to keep intermediate state coherent:
/// 1. Remove duplicate node entries (keep first occurrence of each ID).
/// 2. Remove duplicate edges (keep first occurrence of each triple).
/// 3. Remove self-edges.
/// 4. Remove orphan edges (must be after duplicate-edge dedup so we don't
///    double-count).
/// 5. Remove orphan nodes (last, since removing a node also cascades its edges
///    via `Graph::remove_node` — but by this point those edges are already gone).
///
/// Note: this re-derives the actual mutations from the *current* graph state rather
/// than blindly trusting the plan's IDs. This makes the operation idempotent and
/// safe even if the graph has changed slightly since planning. The plan's flags
/// determine WHICH categories to apply; the per-item lists are advisory.
pub fn apply_repair(graph: &mut Graph, plan: &RepairPlan) -> RepairReport {
    let mut report = RepairReport::default();

    // 1. Duplicate nodes — keep first, drop the rest.
    if !plan.duplicate_node_ids.is_empty() {
        let mut seen: HashSet<String> = HashSet::new();
        let before = graph.nodes.len();
        graph.nodes.retain(|n| seen.insert(n.id.clone()));
        report.duplicate_nodes_merged = before - graph.nodes.len();
    }

    // 2. Duplicate edges — keep first.
    if !plan.duplicate_edges.is_empty() {
        let mut seen: HashSet<(String, String, String)> = HashSet::new();
        let before = graph.edges.len();
        graph
            .edges
            .retain(|e| seen.insert((e.from.clone(), e.to.clone(), e.relation.clone())));
        report.duplicate_edges_removed = before - graph.edges.len();
    }

    // 3. Self-edges.
    if !plan.self_edges.is_empty() {
        let before = graph.edges.len();
        graph.edges.retain(|e| e.from != e.to);
        report.self_edges_removed = before - graph.edges.len();
    }

    // 4. Orphan edges (referencing missing nodes).
    if !plan.orphan_edges.is_empty() {
        let node_ids: HashSet<String> = graph.nodes.iter().map(|n| n.id.clone()).collect();
        let before = graph.edges.len();
        graph
            .edges
            .retain(|e| node_ids.contains(&e.from) && node_ids.contains(&e.to));
        report.orphan_edges_removed = before - graph.edges.len();
    }

    // 5. Orphan nodes (safe types only — already filtered in the plan).
    for id in &plan.orphan_nodes {
        if graph.remove_node(id).is_some() {
            report.orphan_nodes_removed += 1;
        }
    }

    report
}

#[cfg(test)]
mod tests {
    use super::*;
    use crate::graph::{Edge, Node};

    fn code_node(id: &str) -> Node {
        let mut n = Node::new(id, id);
        n.node_type = Some("code".to_string());
        n
    }

    fn task_node(id: &str) -> Node {
        let mut n = Node::new(id, id);
        n.node_type = Some("task".to_string());
        n
    }

    #[test]
    fn plan_orphan_edges_finds_missing_endpoints() {
        let mut g = Graph::new();
        g.add_node(Node::new("a", "A"));
        // Edge to missing 'b'
        g.edges.push(Edge::depends_on("a", "b"));

        let opts = RepairOptions { orphan_edges: true, ..Default::default() };
        let plan = plan_repair(&g, &opts);
        assert_eq!(plan.orphan_edges.len(), 1);
        assert_eq!(
            plan.orphan_edges[0],
            ("a".into(), "b".into(), "depends_on".into())
        );
    }

    #[test]
    fn apply_orphan_edges_removes_them() {
        let mut g = Graph::new();
        g.add_node(Node::new("a", "A"));
        g.edges.push(Edge::depends_on("a", "missing"));
        g.edges.push(Edge::depends_on("missing2", "a"));
        assert_eq!(g.edges.len(), 2);

        let opts = RepairOptions { orphan_edges: true, ..Default::default() };
        let plan = plan_repair(&g, &opts);
        let report = apply_repair(&mut g, &plan);
        assert_eq!(report.orphan_edges_removed, 2);
        assert_eq!(g.edges.len(), 0);
    }

    #[test]
    fn orphan_nodes_only_removes_safe_types() {
        let mut g = Graph::new();
        g.add_node(code_node("file:foo.rs")); // safe — orphan
        g.add_node(task_node("iss-001")); // unsafe — orphan but skipped
        g.add_node(code_node("file:bar.rs")); // connected
        g.add_node(code_node("file:baz.rs")); // connected target
        g.add_edge(Edge::depends_on("file:bar.rs", "file:baz.rs"));

        let opts = RepairOptions { orphan_nodes: true, ..Default::default() };
        let plan = plan_repair(&g, &opts);
        assert_eq!(plan.orphan_nodes, vec!["file:foo.rs".to_string()]);
        assert_eq!(plan.skipped_unsafe_orphan_nodes.len(), 1);
        assert_eq!(plan.skipped_unsafe_orphan_nodes[0].0, "iss-001");

        let report = apply_repair(&mut g, &plan);
        assert_eq!(report.orphan_nodes_removed, 1);
        // Task node MUST still exist
        assert!(g.nodes.iter().any(|n| n.id == "iss-001"));
        assert!(!g.nodes.iter().any(|n| n.id == "file:foo.rs"));
    }

    #[test]
    fn duplicate_nodes_keep_first() {
        let mut g = Graph::new();
        g.nodes.push(Node::new("a", "First"));
        g.nodes.push(Node::new("a", "Second"));
        g.nodes.push(Node::new("a", "Third"));
        g.add_node(Node::new("b", "B"));

        let opts = RepairOptions { duplicate_nodes: true, ..Default::default() };
        let plan = plan_repair(&g, &opts);
        assert_eq!(plan.duplicate_node_ids.len(), 2); // "a" duplicated twice

        let report = apply_repair(&mut g, &plan);
        assert_eq!(report.duplicate_nodes_merged, 2);
        assert_eq!(g.nodes.iter().filter(|n| n.id == "a").count(), 1);
        // First occurrence kept
        assert_eq!(g.nodes.iter().find(|n| n.id == "a").unwrap().title, "First");
    }

    #[test]
    fn duplicate_edges_keep_first() {
        let mut g = Graph::new();
        g.add_node(Node::new("a", "A"));
        g.add_node(Node::new("b", "B"));
        // Bypass add_edge dedup by pushing directly
        g.edges.push(Edge::depends_on("a", "b"));
        g.edges.push(Edge::depends_on("a", "b"));
        g.edges.push(Edge::depends_on("a", "b"));

        let opts = RepairOptions { duplicate_edges: true, ..Default::default() };
        let plan = plan_repair(&g, &opts);
        assert_eq!(plan.duplicate_edges.len(), 2);

        let report = apply_repair(&mut g, &plan);
        assert_eq!(report.duplicate_edges_removed, 2);
        assert_eq!(g.edges.len(), 1);
    }

    #[test]
    fn self_edges_removed() {
        let mut g = Graph::new();
        g.add_node(Node::new("a", "A"));
        g.add_node(Node::new("b", "B"));
        g.edges.push(Edge::depends_on("a", "a"));
        g.add_edge(Edge::depends_on("a", "b"));

        let opts = RepairOptions { self_edges: true, ..Default::default() };
        let plan = plan_repair(&g, &opts);
        assert_eq!(plan.self_edges.len(), 1);

        let report = apply_repair(&mut g, &plan);
        assert_eq!(report.self_edges_removed, 1);
        assert_eq!(g.edges.len(), 1);
    }

    #[test]
    fn empty_plan_is_noop() {
        let mut g = Graph::new();
        g.add_node(Node::new("a", "A"));
        g.add_node(Node::new("b", "B"));
        g.add_edge(Edge::depends_on("a", "b"));

        let opts = RepairOptions::all();
        let plan = plan_repair(&g, &opts);
        assert!(plan.is_empty());

        let report = apply_repair(&mut g, &plan);
        assert_eq!(report.total(), 0);
        assert_eq!(g.nodes.len(), 2);
        assert_eq!(g.edges.len(), 1);
    }

    #[test]
    fn all_options_combined() {
        let mut g = Graph::new();
        g.add_node(code_node("file:a.rs"));
        g.add_node(code_node("file:b.rs"));
        g.add_node(code_node("file:orphan.rs"));
        g.nodes.push(code_node("file:a.rs")); // duplicate
        g.add_edge(Edge::depends_on("file:a.rs", "file:b.rs"));
        g.edges.push(Edge::depends_on("file:a.rs", "file:b.rs")); // duplicate edge
        g.edges.push(Edge::depends_on("file:b.rs", "missing")); // orphan edge
        g.edges.push(Edge::depends_on("file:a.rs", "file:a.rs")); // self-edge

        let opts = RepairOptions::all();
        let plan = plan_repair(&g, &opts);
        let report = apply_repair(&mut g, &plan);

        assert_eq!(report.duplicate_nodes_merged, 1);
        assert_eq!(report.duplicate_edges_removed, 1);
        assert_eq!(report.self_edges_removed, 1);
        assert_eq!(report.orphan_edges_removed, 1);
        assert_eq!(report.orphan_nodes_removed, 1);

        // After repair: 2 nodes (a, b), 1 edge (a -> b)
        assert_eq!(g.nodes.len(), 2);
        assert_eq!(g.edges.len(), 1);

        // Re-validate: should be clean now
        let opts = RepairOptions::all();
        let replan = plan_repair(&g, &opts);
        assert!(
            replan.is_empty(),
            "graph should be clean after repair, got: {}",
            replan
        );
    }

    #[test]
    fn plan_display_when_empty() {
        let plan = RepairPlan::default();
        assert!(plan.is_empty());
        assert_eq!(plan.to_string(), "✓ Nothing to repair");
    }

    #[test]
    fn plan_display_truncates_long_lists() {
        let mut plan = RepairPlan::default();
        for i in 0..10 {
            plan.orphan_edges.push((
                format!("a{}", i),
                format!("b{}", i),
                "depends_on".into(),
            ));
        }
        let s = plan.to_string();
        assert!(s.contains("Remove 10 orphan edge"));
        assert!(s.contains("and 5 more"));
    }

    #[test]
    fn report_display_zero() {
        let r = RepairReport::default();
        assert_eq!(r.to_string(), "✓ No changes applied");
    }

    #[test]
    fn options_any_and_all() {
        assert!(!RepairOptions::default().any());
        assert!(RepairOptions::all().any());
        let opts = RepairOptions { orphan_edges: true, ..Default::default() };
        assert!(opts.any());
    }
}
